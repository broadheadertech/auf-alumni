function Composer({
  name,
  draft,
  setDraft,
  visibility,
  setVisibility,
  onPublish,
  submitting,
}: {
  name: string;
  draft: string;
  setDraft: (v: string) => void;
  visibility: "connections" | "alumni";
  setVisibility: (v: "connections" | "alumni") => void;
  onPublish: () => void;
  submitting: boolean;
}) {
  return (
    <div className="auf-card p-4">
      <div className="flex items-center gap-3">
        <AUFAvatar name={name} grad={1} size={40} />
        <textarea
          rows={1}
          maxLength={2000}
          placeholder="Share an update, a job, or ask for help…"
          value={draft}
          onChange={(e) => setDraft(e.target.value)}
          className="flex-1 text-[14px] px-4 py-2.5 rounded-full bg-[var(--surface-2)] hover:bg-[var(--brand-50)] transition resize-none focus:outline-none focus:ring-2 focus:ring-[var(--brand)]"
        />
      </div>
      {draft.trim() && (
        <div className="flex items-center justify-between gap-2 mt-3 pt-3 border-t auf-hairline">
          <select
            value={visibility}
            onChange={(e) =>
              setVisibility(e.target.value as "connections" | "alumni")
            }
            className="auf-chip cursor-pointer"
          >
            <option value="connections">Connections only</option>
            <option value="alumni">All verified alumni</option>
          </select>
          <button
            className="auf-btn auf-btn-primary auf-btn-sm"
            onClick={onPublish}
            disabled={!draft.trim() || submitting}
          >
            {submitting ? (
              <Loader2 className="h-3.5 w-3.5 animate-spin" />
            ) : (
              <Send className="h-3.5 w-3.5" />
            )}
            Post
          </button>
        </div>
      )}
    </div>
  );
}

function FeedPost({
  post,
  mine,
  onDelete,
}: {
  post: Post;
  mine: boolean;
  onDelete: (id: string) => void;
}) {
  return (
    <div className="auf-card p-5">
      <div className="flex items-start gap-3">
        <AUFAvatar name={post.authorName ?? "?"} grad={3} badge size={44} />
        <div className="flex-1 min-w-0">
          <div className="flex flex-wrap items-center gap-2">
            {post.authorSlug ? (
              <Link
                href={`/profile/${post.authorSlug}`}
                className="font-medium text-[14.5px] hover:underline"
              >
                {post.authorName ?? "Unknown alumna"}
              </Link>
            ) : (
              <span className="font-medium text-[14.5px]">
                {post.authorName ?? "Unknown alumna"}
              </span>
            )}
            {post.autoGenerated && (
              <span className="auf-chip auf-chip-brand">
                <Sparkles size={11} /> milestone
              </span>
            )}
          </div>
          <div className="text-[12.5px] ink-3 mt-0.5">
            · {relativeTime(post.createdAt)}
          </div>
        </div>
        {mine && !post.autoGenerated && (
          <button
            onClick={() => onDelete(post._id)}
            className="auf-btn auf-btn-ghost auf-btn-sm"
            aria-label="Delete post"
          >
            <Trash2 className="h-3.5 w-3.5" />
          </button>
        )}
      </div>
      <p className="mt-3 text-[14.5px] leading-[1.55] whitespace-pre-wrap ink">
        {post.content}
      </p>
      {post.imageUrl && (
        // eslint-disable-next-line @next/next/no-img-element
        <img
          src={post.imageUrl}
          alt=""
          className="mt-3 max-h-96 rounded-lg border auf-hairline object-cover w-full"
        />
      )}
    </div>
  );
}

function RightCardJobs({ jobs }: { jobs: Job[] }) {
  return (
    <div className="auf-card p-5">
      <div className="flex items-center justify-between mb-4">
        <div>
          <div className="section-eyebrow">Matched for you</div>
          <h3 className="font-serif text-[17px] font-semibold mt-0.5">
            Jobs from your network
          </h3>
        </div>
        <Link
          href="/jobs"
          className="text-[12px] brand-fg font-medium hover:underline"
        >
          All →
        </Link>
      </div>
      {jobs.length === 0 ? (
        <p className="text-[12.5px] ink-3">
          No matched jobs yet — check the Jobs tab.
        </p>
      ) : (
        <div className="space-y-3">
          {jobs.slice(0, 3).map((j, i) => {
            const initials = (j.employerName ?? "?")
              .split(/\s+/)
              .map((w) => w[0])
              .slice(0, 2)
              .join("");
            const grad = (((i % 6) + 1) as 1 | 2 | 3 | 4 | 5 | 6);
            return (
              <Link
                key={j._id}
                href={`/jobs`}
                className="w-full flex items-start gap-3 p-2.5 -mx-2 rounded-lg hover:bg-[var(--surface-2)] transition"
              >
                <div
                  className={`w-9 h-9 rounded-lg av-grad-${grad} text-white font-serif font-semibold text-sm flex items-center justify-center shrink-0`}
                >
                  {initials}
                </div>
                <div className="flex-1 min-w-0">
                  <div className="text-[13.5px] font-medium truncate">
                    {j.title}
                  </div>
                  <div className="text-[12px] ink-3 truncate">
                    {j.employerName} · {j.location?.split(" · ")[0]}
                  </div>
                </div>
              </Link>
            );
          })}
        </div>
      )}
    </div>
  );
}

function RightCardEvents({ events }: { events: EventRow[] }) {
  return (
    <div className="auf-card p-5">
      <div className="section-eyebrow mb-3">Upcoming</div>
      {events.length === 0 ? (
        <p className="text-[12.5px] ink-3">No upcoming events.</p>
      ) : (
        <div className="space-y-3">
          {events.slice(0, 3).map((e) => {
            const d = new Date(e.startsAt);
            const month = d
              .toLocaleString("en-US", { month: "short" })
              .toUpperCase();
            const day = d.getDate();
            return (
              <Link
                key={e._id}
                href="/events"
                className="flex items-start gap-3 -mx-2 px-2 py-1 rounded-md hover:bg-[var(--surface-2)]"
              >
                <div className="text-center shrink-0 w-12 px-2 py-1.5 rounded-md border auf-hairline">
                  <div className="text-[10px] font-semibold ink-3 tracking-wider">
                    {month}
                  </div>
                  <div className="text-[16px] font-serif font-semibold leading-none mt-0.5">
                    {day}
                  </div>
                </div>
                <div className="flex-1 min-w-0">
                  <div className="text-[13.5px] font-medium leading-tight">
                    {e.title}
                  </div>
                  <div className="text-[12px] ink-3 mt-0.5">
                    {e.locationLabel ?? e.onlineUrl ?? "Online"}
                  </div>
                  <div className="text-[11px] ink-3 mt-1">
                    {e.goingCount} alumni going
                  </div>
                </div>
              </Link>
            );
          })}
        </div>
      )}
    </div>
  );
}

