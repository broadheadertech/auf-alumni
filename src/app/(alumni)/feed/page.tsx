"use client";

/**
 * Feed page (ported from Claude Design prototype).
 *
 * Two-column layout: main feed (greeting hero + composer + posts) + right
 * rail (matched jobs, upcoming events, suggested people, footer).
 * Composer is always visible to authenticated users; on empty feed we still
 * show composer + rail rather than a dead-end empty state.
 */

import { useState } from "react";
import Link from "next/link";
import { useMutation, useQuery } from "convex/react";
import { toast } from "sonner";
import {
  ArrowRight,
  Briefcase,
  Calendar,
  Loader2,
  Send,
  Sparkles,
  Trash2,
  Users,
} from "lucide-react";
import { api } from "@/lib/convex-api";
import { AUFAvatar } from "@/components/auf/AUFAvatar";

type Post = {
  _id: string;
  authorId: string;
  authorName: string | null;
  authorSlug: string | null;
  kind: string;
  content: string;
  imageUrl: string | null;
  autoGenerated: boolean;
  createdAt: number;
};

type Job = {
  _id: string;
  title: string;
  employerName: string;
  location: string;
  salaryMin?: number;
  salaryMax?: number;
  salaryCurrency?: string;
};

type EventRow = {
  _id: string;
  title: string;
  startsAt: number;
  locationLabel?: string;
  onlineUrl?: string;
  goingCount: number;
};

type Suggested = {
  _id: string;
  displayName: string;
  currentRole?: string;
  company?: string;
};

function relativeTime(ms: number): string {
  const diff = Date.now() - ms;
  const minutes = Math.round(diff / 60000);
  if (minutes < 1) return "just now";
  if (minutes < 60) return `${minutes}m`;
  const hours = Math.round(minutes / 60);
  if (hours < 24) return `${hours}h`;
  const days = Math.round(hours / 24);
  if (days < 7) return `${days}d`;
  return new Date(ms).toLocaleDateString();
}

function firstName(name?: string | null): string {
  if (!name) return "there";
  return name.split(/\s+/)[0] ?? "there";
}

function greeting(): string {
  const h = new Date().getHours();
  if (h < 12) return "Good morning";
  if (h < 18) return "Good afternoon";
  return "Good evening";
}

export default function FeedPage() {
  const me = useQuery(api.users.getMe);
  const myProfile = useQuery(api.profiles.getMyProfile);
  const feed = useQuery(api.feed.getFeed, { limit: 30 });
  const matchedJobs = useQuery(api.jobs.browse, { showUnmatched: true });
  const upcomingEvents = useQuery(api.events.listUpcoming);
  const suggested = useQuery(api.directory.list, { pageSize: 3 });
  const publishPost = useMutation(api.feed.publishPost);
  const deletePost = useMutation(api.feed.deletePost);

  const [draft, setDraft] = useState("");
  const [visibility, setVisibility] = useState<"connections" | "alumni">(
    "connections",
  );
  const [submitting, setSubmitting] = useState(false);

  const onPublish = async () => {
    if (!draft.trim()) return;
    setSubmitting(true);
    try {
      await publishPost({ content: draft.trim(), visibilityTier: visibility });
      toast.success("Posted");
      setDraft("");
    } catch (err) {
      toast.error(err instanceof Error ? err.message : "Could not post");
    } finally {
      setSubmitting(false);
    }
  };

  const onDelete = async (postId: string) => {
    try {
      await deletePost({ postId: postId as unknown as never });
      toast.success("Deleted");
    } catch (err) {
      toast.error(err instanceof Error ? err.message : "Delete failed");
    }
  };

  const posts = (feed ?? []) as Post[];
  type MeSnapshot = { _id: string; name?: string; email?: string } | null;
  type ProfileSnapshot = { displayName?: string } | null;
  const meSnap = (me ?? null) as MeSnapshot;
  const profileSnap = (myProfile ?? null) as ProfileSnapshot;
  const personName =
    profileSnap?.displayName ?? meSnap?.name ?? meSnap?.email ?? null;

  return (
    <div className="grid grid-cols-1 lg:grid-cols-12 gap-4 lg:gap-6 px-4 sm:px-7 py-6">
      {/* Main column */}
      <div className="col-span-12 lg:col-span-8 space-y-5">
        <GreetingHero name={personName} />
        {meSnap && (
          <Composer
            name={personName ?? "you"}
            draft={draft}
            setDraft={setDraft}
            visibility={visibility}
            setVisibility={setVisibility}
            onPublish={onPublish}
            submitting={submitting}
          />
        )}

        {/* Posts */}
        <div className="space-y-4">
          {feed === undefined ? (
            <Loader2 className="mx-auto h-6 w-6 animate-spin ink-3" />
          ) : (
            posts.map((p) => (
              <FeedPost
                key={p._id}
                post={p}
                mine={!!meSnap && p.authorId === meSnap._id}
                onDelete={onDelete}
              />
            ))
          )}
        </div>
      </div>

      {/* Right rail */}
      <aside className="col-span-12 lg:col-span-4 space-y-5">
        <RightCardJobs jobs={(matchedJobs ?? []) as Job[]} />
        <RightCardEvents events={(upcomingEvents ?? []) as EventRow[]} />
        <RightCardSuggested
          suggested={
            (((suggested as { items?: Suggested[] } | undefined)?.items) ??
              []) as Suggested[]
          }
        />
        <FooterCard />
      </aside>
    </div>
  );
}

